import streamlit as st

# Конфигурация на страницата
st.set_page_config(
    page_title="Перото и Сабята - Възрожденският Герой",
    page_icon="✒️",
    layout="wide"
)

# Заглавие в страничното меню
st.sidebar.title("📌 Навигация")
page = st.sidebar.radio("Изберете секция:", [
    "🏠 Начало", 
    "📚 Еволюция на Героя", 
    "⚔️ Видове Герои", 
    "📝 Тест за класа"
])

# --- СЕКЦИЯ 1: НАЧАЛО ---
if page == "🏠 Начало":
    st.title("✒️ Между перото и сабята")
    st.subheader("Как се ражда възрожденският герой?")
    
    st.markdown("""
    Българското Възраждане е уникален период, в който националното съзнание се пробужда чрез силата на писаното слово (перото) и се реализира чрез силата на революцията (сабята). 
    
    Този уебсайт изследва как българската литература извървява пътя от тъмната манастирска килия до върховете на Балкана, за да извае образа на истинския герой.
    """)
    
    col1, col2 = st.columns(2)
    with col1:
        st.info("### 📝 Перото (Просвета и Памет)\nСимволизира етапа на осъзнаване, научаване на историята и борба за църковна и културна независимост. Представители: Паисий и Славейков.")
    with col2:
        st.error("### ⚔️ Сабята (Бунт и Саможертва)\nСимволизира етапа на радикалното действие, в който свободата се превръща в най-висша ценност, по-важна от самия живот. Представител: Ботев.")

# --- СЕКЦИЯ 2: ЕВОЛЮЦИЯ НА ГЕРОЯ ---
elif page == "📚 Еволюция на Героя":
    st.title("📈 Развитието на възрожденския герой")
    st.write("Разгледайте как се променя дефиницията за 'герой' при тримата ключови автори:")

    tab1, tab2, tab3 = st.tabs(["Паисий Хилендарски", "Петко Р. Славейков", "Христо Ботев"])
    
    with tab1:
        st.header("Паисий Хилендарски — 'История славянобългарска'")
        st.markdown(""" Кой е героят? Героят за Паисий е будителят, книжовникът и пазителят на историческата памет. Същност: Това е човекът, който се бори срещу чуждото културно влияние не с оръжие, а с истината за миналото. Герои са и средновековните български царе и светци, които доказват, че народът ни има право на гордост и собствено бъдеще. Ключово послание: Осъзнаването на националната идентичност е първата крачка към свободата. """)
        
    with tab2:
        st.header("Петко Р. Славейков — 'Изворът на белоногата'")
        st.markdown(""" Кой е героят? Героят тук е пазителят на българските традиции, морал и родното пространство. Същност: Образът на Гергана преобръща традиционната представа за физическа сила. Тя е морален победител. Нейният героизъм се изразява в категоричния отказ да замени родния дом, градинката и любовта си към Никола за разкоша на османските палати. Ключово послание: Верността към корена и вътрешната свобода са непревземаема крепост за всеки завоевател. """)
        
    with tab3:
        st.header("Христо Ботев — Поетическото творчество")
        st.markdown(""" Кой е героят? Героят за Ботев е революционерът, бунтовникът и тираноборецът. Същност: Това е титанът, който извървява пътя от словото до саможертвата на бойното поле. Ботевският герой съзнателно избира смъртта в името на свободата. Този акт го извежда извън рамките на земното време и го превръща в безсмъртен мит. Ключово послание: 'Тоз, който падне в бой за свобода, той не умира...' – саможертвата е върховният израз на човешкото достойнство. """)

# --- СЕКЦИЯ 3: ВИДОВЕ ГЕРОИ ---
elif page == "⚔️ Видове Герои":
    st.title("🔍 Сравнителен анализ на героите")
    st.write("Възрожденската епоха си служи с различни типове образи. Ето как се съпоставят те:")
    
    # Сравнителна таблица
    st.markdown("""
| Критерий | Исторически герой | Литературен герой | Народен герой |
| :--- | :--- | :--- | :--- |
| Произход | Реален, документално доказан | Създаден от авторовото въображение | Колективно творчество (фолклор) |
| Основно оръжие | Дела, събития, реални битки | Послания, идеи и емоции | Свръхестествена сила, митове |
| Пример | Васил Левски, Паисий | Гергана, Ботевият лирически аз | Крали Марко, Момчил воевода |
""")

# --- СЕКЦИЯ 4: ТЕСТ ЗА КЛАСА ---
elif page == "📝 Тест за класа":
    st.title("🧠 Проверете знанията си")
    st.write("Бърз интерактивен куиз върху темата:")
    
    # Въпрос 1
    q1 = st.radio("1. Кой е основният герой според Паисий Хилендарски?", 
                  ["Революционерът със сабя в ръка", "Будителят и пазителят на паметта", "Търговецът, който пътува по света"])
    if st.button("Провери Въпрос 1"):
        if q1 == "Будителят и пазителят на паметта":
            st.success("Точно така! За Паисий най-важно е народът да се събуди и да научи историята си.")
        else:
            st.error("Грешно. Опитайте пак! Жокер: Свързано е с 'перото'.")
            
    # Въпрос 2
    st.write("---")
    q2 = st.radio("2. В какво се изразява героизмът на Гергана в 'Изворът на белоногата'?", 
                  ["Във физическа битка с везира", "В защитата на моралните ценности и верността към корена", "В бягство в чужбина"])
    if st.button("Провери Въпрос 2"):
        if q2 == "В защитата на моралните ценности и верността към корена":
            st.success("Вярно! Тя постига морална победа, отказвайки палатите на пашата.")
        else:
            st.error("Грешно. Прочетете пак анализа на Петко Славейков.")

    # Въпрос 3
    st.write("---")
    q3 = st.radio("3. Какво носи безсмъртие на Ботевия герой?", 
                  ["Богатството, което е натрупал", "Саможертвата в бой за свобода", "Дългият и спокоен живот"])
    if st.button("Провери Въпрос 3"):
        if q3 == "Саможертвата в бой за свобода":
            st.success("Абсолютно вярно! 'Тоз, който падне в бой за свобода, той не умира...'")
        else:
            st.error("Гreшно. Ботев възпява саможертвения бунт.")
